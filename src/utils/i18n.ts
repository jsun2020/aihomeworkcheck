export const getCurrentLanguage = (userId: string): 'zh-CN' | 'en-US' => {
  const savedSettings = localStorage.getItem(`userSettings_${userId}`);
  if (savedSettings) {
    const settings = JSON.parse(savedSettings);
    return settings.language || 'en-US';
  }
  return 'en-US';
};